// 4-way serial A/D + 4 way multiplexer for 6 analog inputs total

static UINT8 input[4];

static READ16_HANDLER( iod_r )
{
	UINT16 res = input[offset] & 0x80;
	input[offset] <<= 1;
	return res;
}

static WRITE16_HANDLER( iod_w )
{
	switch(offset & 3) {
	case 0: // 3 "normal" analog ports
	case 1:
	case 2:
		input[offset] = readinputport(offset);
		break;
	case 3: // 3 more ports multiplexed in, 4th is grounded
	{
		int port = control[4] & 3;
		input[offset] = port != 3 ? readinputport(port + 3) : 0x00;
		break;
	}
	}
}

static WRITE16_HANDLER( yboard_snd_w )
{
	if (ACCESSING_LSB) {
		soundlatch_w(0, data&0xff);
		cpunum_set_input_line(3, INPUT_LINE_NMI, PULSE_LINE);
	}
}

static void fast_timer_callback(int param)
{
    if (--param)
        timer_set(TIME_IN_USEC(2), param, fast_timer_callback);
}

static int irq2_toggle;
static int irq2_hack;

static INTERRUPT_GEN(yboard_interrupt)
{
	if(cpu_getiloops()) {
		if((!irq2_hack) || !irq2_toggle) {
			cpunum_set_input_line(0, 2, HOLD_LINE);
			cpunum_set_input_line(1, 2, HOLD_LINE);
			cpunum_set_input_line(2, 2, HOLD_LINE);
			timer_set(TIME_IN_USEC(2), 100, fast_timer_callback);
		}
		irq2_toggle = !irq2_toggle;
	} else {
		cpunum_set_input_line(0, 4, HOLD_LINE);
		cpunum_set_input_line(1, 4, HOLD_LINE);
		cpunum_set_input_line(2, 4, HOLD_LINE);
		timer_set(TIME_IN_USEC(2), 100, fast_timer_callback);
	}
}


