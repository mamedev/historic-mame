// 5 bidirectional digital ports used plus one for internal control

static unsigned char control[8];

static UINT8 io_r(int port)
{
	switch(port) {
	case 0:
		return readinputport(6);
	case 1:
		return readinputport(7);
	case 2:
		return readinputport(8);
	case 3:
		return readinputport(9);
	case 4: // control
		return control[port];
	case 5: // Dip switches
		return readinputport(10);
	case 6:
		return readinputport(11);
	case 7: // Motor control and mute, output only
		return 0x00;
	}
	return 0x00;
}

